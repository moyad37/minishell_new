#include "libft.h"

void	ft_free_list(t_node **head)
{
	t_node	*current;
	t_node	*next;

	if (!head)
		return ;
	current = *head;
	while (current)
	{
		next = current->next;
		if (current->key)
			free(current->key);
		if (current->value)
			free(current->value);
		if (current->env_line)
			free(current->env_line);
		free(current);
		current = next;
	}
	*head = NULL;
}
